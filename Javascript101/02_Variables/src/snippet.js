//The '+' operator can  be used to add (concatenate) strings.
var string1 = "Hello ";
var string2 = "world!";
var result1 = string1 + string2;
console.log("result1 is "+result1);

//Using concat() method on string variable to join two strings
var string3 = "Hello ";
var string4 = "world!";
var result2 = string3.concat(string4);
console.log("result2 is "+result2);
